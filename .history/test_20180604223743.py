rng = list(range(100,1000))
rng.reverse()
n = 0
print("These are top 25 three-digit numbers divisible by 7 or 9 (wtf):")
for i in rng:
    if i%9 == 0:
        print(str(i) + " by 9") 
        n += 1
    if i%7 == 0:
        print(str(i) + " by 7")
        n += 1
    if n >= 25:
        print("Thank you for your attention.")
        break
